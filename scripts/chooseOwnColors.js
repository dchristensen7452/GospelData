//Triple
$(document).ready(function(){	
	$(".Tcolorsel").click(function(){
		var paint = this.name;
		//console.log(paint);
	$(".T").css({color: paint});
	$(".T1").css({'text-decoration': 'underline',
		'text-decoration-color': paint});
	});
});
$(document).ready(function(){	
	$(".Tcolorsel-none").click(function(){
		var paint = this.name;
		//console.log(paint);
	$(".T").css({color: 'black'});
	$(".T1").css({'text-decoration': 'none',
		'text-decoration-color': 'none'});
	});
});
